package basicJava;

public class ArithmeticOper {
    public static void main(String[] args) {
        int x = 1 ;
        int y = 2;
        System.out.println(x = x+y);
        System.out.println(x= x-y);//  note : x is incremented in the previous statements.
        System.out.println(x=x*y);
        System.out.println(x=x/y);
        System.out.println(x=x%y);
        //order of operation {P E  M D A S} left to right





    }






}
